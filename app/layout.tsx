import type { Metadata } from 'next';
import './globals.css';
import { Inter } from 'next/font/google';
import { SerwistProvider } from './serwist';

export const metadata: Metadata = {
  title: 'Working App',
  description: 'Generated by Working App',
  manifest: '/manifest.webmanifest',
  themeColor: '#000000',
  appleWebApp: {
    capable: true,
    title: 'Working Time',
    statusBarStyle: 'default',
  },
};

const inter = Inter({ subsets: ['latin'] });

export default function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  return (
    <html suppressHydrationWarning>
      <head>
        <script
          dangerouslySetInnerHTML={{
            __html: `
              if (!navigator.onLine) {
                document.documentElement.classList.add('is-offline');
              }
              window.addEventListener('offline', function() { document.documentElement.classList.add('is-offline'); });
              window.addEventListener('online', function() { document.documentElement.classList.remove('is-offline'); });
            `,
          }}
        />
      </head>
      <body
        className={`${inter.className} antialiased bg-[#DBDBDB] dark:bg-[#12182a] max-h-dvh overflow-hidden`}>
        <SerwistProvider swUrl='/serwist/sw.js'>{children}</SerwistProvider>
      </body>
    </html>
  );
}
